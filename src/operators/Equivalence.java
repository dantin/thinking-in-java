public class Equivalence {

    public static void main(String[] args) {
	Integer n1 = new Integer(47);
	Integer n2 = new Integer(47);
	System.out.println("new Integer n1 = " + n1 + ", new Integer n2 = " + n2);
	System.out.println("n1 == n2: " + (n1 == n2));
	System.out.println("n1 != n2: " + (n1 != n2));
    }

}
